type RuntimeStats struct {
	StartTime time.Time

	ActiveConn int64 // atomic

	FramesRx                uint64 // atomic
	AcksTx                  uint64 // atomic
	MQTTPubOK               uint64 // atomic
	MQTTPubErr              uint64 // atomic
	RejectedAccountMismatch uint64 // atomic

	mu                  sync.RWMutex
	LastState           AlarmState
	LastCode            string
	LastUser            string
	LastEventRaw        string
	LastEventTime       time.Time
	LastMQTTPublishTime time.Time
}

type PersistedState struct {
	State         AlarmState `json:"state"`
	Code          string     `json:"code"`
	User          string     `json:"user"`
	LastEventTime time.Time  `json:"last_event_time"`
}

func (s *RuntimeStats) UpdateEvent(state AlarmState, code, user, raw string) {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.LastState = state
	s.LastCode = code
	s.LastUser = user
	s.LastEventRaw = raw
	s.LastEventTime = time.Now()
}

func (s *RuntimeStats) Snapshot() PersistedState {
	s.mu.RLock()
	defer s.mu.RUnlock()
	return PersistedState{
		State:         s.LastState,
		Code:          s.LastCode,
		User:          s.LastUser,
		LastEventTime: s.LastEventTime,
	}
}

func (s *RuntimeStats) ApplyPersistedState(p PersistedState) {
	s.mu.Lock()
	defer s.mu.Unlock()
	if p.State != "" {
		s.LastState = p.State
	}
	if p.Code != "" {
		s.LastCode = p.Code
	}
	if p.User != "" {
		s.LastUser = p.User
	}
	if !p.LastEventTime.IsZero() {
		s.LastEventTime = p.LastEventTime
	}
}

func (s *RuntimeStats) UpdateMQTTPublish(ok bool) {
	if ok {
		atomic.AddUint64(&s.MQTTPubOK, 1)
	} else {
		atomic.AddUint64(&s.MQTTPubErr, 1)
	}
	s.mu.Lock()
	s.LastMQTTPublishTime = time.Now()
	s.mu.Unlock()
}

