func startHTTP(cfg Config, pub *Publisher, stats *RuntimeStats) {
	mux := http.NewServeMux()

	mux.HandleFunc("/health", func(w http.ResponseWriter, _ *http.Request) {
		w.Header().Set("Content-Type", "text/plain; charset=utf-8")
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write([]byte("ok\n"))
	})

	mux.HandleFunc("/state", func(w http.ResponseWriter, _ *http.Request) {
		stats.mu.RLock()
		lastState := stats.LastState
		lastCode := stats.LastCode
		lastUser := stats.LastUser
		lastEventTime := stats.LastEventTime
		stats.mu.RUnlock()

		out := map[string]any{
			"state":           lastState,
			"code":            lastCode,
			"user":            lastUser,
			"last_event_time": zeroOrRFC3339(lastEventTime),
		}

		w.Header().Set("Content-Type", "application/json; charset=utf-8")
		enc := json.NewEncoder(w)
		enc.SetIndent("", "  ")
		_ = enc.Encode(out)
	})

	mux.HandleFunc("/stats", func(w http.ResponseWriter, _ *http.Request) {
		uptime := time.Since(stats.StartTime)

		stats.mu.RLock()
		lastState := stats.LastState
		lastCode := stats.LastCode
		lastUser := stats.LastUser
		lastRaw := stats.LastEventRaw
		lastEventTime := stats.LastEventTime
		lastMQTTPubTime := stats.LastMQTTPublishTime
		stats.mu.RUnlock()

		out := map[string]any{
			"uptime_seconds": int64(uptime.Seconds()),
			"start_time":     stats.StartTime.Format(time.RFC3339),

			"active_connections": atomic.LoadInt64(&stats.ActiveConn),

			"frames_rx":                 atomic.LoadUint64(&stats.FramesRx),
			"acks_tx":                   atomic.LoadUint64(&stats.AcksTx),
			"rejected_account_mismatch": atomic.LoadUint64(&stats.RejectedAccountMismatch),

			"mqtt_connected": pub.IsConnected(),
			"mqtt_pub_ok":    atomic.LoadUint64(&stats.MQTTPubOK),
			"mqtt_pub_err":   atomic.LoadUint64(&stats.MQTTPubErr),

			"last_state":         lastState,
			"last_code":          lastCode,
			"last_user":          lastUser,
			"last_event_time":    zeroOrRFC3339(lastEventTime),
			"last_mqtt_pub_time": zeroOrRFC3339(lastMQTTPubTime),
			"last_event_raw":     lastRaw,

			"ha_discovery_state_topic": pub.discoveryConfigTopic(),
			"ha_discovery_user_topic":  pub.userDiscoveryConfigTopic(),
			"ha_unique_id":             pub.uniqueID(),
		}

		w.Header().Set("Content-Type", "application/json; charset=utf-8")
		enc := json.NewEncoder(w)
		enc.SetIndent("", "  ")
		_ = enc.Encode(out)
	})

	srv := &http.Server{
		Addr:              cfg.HTTPListenAddr,
		Handler:           mux,
		ReadHeaderTimeout: 5 * time.Second,
	}

	go func() {
		log.Printf("[%s] HTTP stats listening on http://%s (endpoints: /health, /state, /stats)\n",
			time.Now().Format("15:04:05"), cfg.HTTPListenAddr)
		if err := srv.ListenAndServe(); err != nil && !errors.Is(err, http.ErrServerClosed) {
			log.Printf("[%s] HTTP server error: %v\n", time.Now().Format("15:04:05"), err)
		}
	}()
}

func zeroOrRFC3339(t time.Time) any {
	if t.IsZero() {
		return nil
	}
	return t.Format(time.RFC3339)
}

func loadPersistedState(path string, stats *RuntimeStats) error {
	if path == "" {
		return nil
	}
	body, err := os.ReadFile(path)
	if err != nil {
		if errors.Is(err, os.ErrNotExist) {
			return nil
		}
		return err
	}
	var p PersistedState
	if err := json.Unmarshal(body, &p); err != nil {
		return err
	}
	stats.ApplyPersistedState(p)
	return nil
}

func persistState(path string, stats *RuntimeStats) error {
	if path == "" {
		return nil
	}
	state := stats.Snapshot()
	if state.State == "" {
		return nil
	}
	dir := filepath.Dir(path)
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	body, err := json.MarshalIndent(state, "", "  ")
	if err != nil {
		return err
	}
	tmp, err := os.CreateTemp(dir, "state-*.json")
	if err != nil {
		return err
	}
	tmpName := tmp.Name()
	_, writeErr := tmp.Write(append(body, '\n'))
	closeErr := tmp.Close()
	if writeErr != nil {
		_ = os.Remove(tmpName)
		return writeErr
	}
	if closeErr != nil {
		_ = os.Remove(tmpName)
		return closeErr
	}
	if err := os.Rename(tmpName, path); err != nil {
		_ = os.Remove(tmpName)
		return err
	}
	return nil
}

