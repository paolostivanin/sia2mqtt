func handleConnection(conn net.Conn, cfg Config, pub *Publisher, stats *RuntimeStats) {
	defer conn.Close()

	atomic.AddInt64(&stats.ActiveConn, 1)
	defer atomic.AddInt64(&stats.ActiveConn, -1)

	ra := conn.RemoteAddr().String()
	log.Printf("[%s] CONNECT %s\n", time.Now().Format("15:04:05"), ra)

	r := bufio.NewReaderSize(conn, 64*1024)

	for {
		_ = conn.SetReadDeadline(time.Now().Add(60 * time.Second))

		frame, err := readFrame(r)
		if err != nil {
			if ne, ok := err.(net.Error); ok && ne.Timeout() {
				continue
			}
			log.Printf("[%s] DISCONNECT %s err=%v\n", time.Now().Format("15:04:05"), ra, err)
			return
		}
		if len(frame) == 0 {
			continue
		}

		atomic.AddUint64(&stats.FramesRx, 1)

		raw := string(frame)
		log.Printf("\n[%s] RECV %s: %s\n", time.Now().Format("15:04:05"), ra, raw)
		if cfg.Verbose {
			log.Printf("[%s] RECV_HEX: %s\n", time.Now().Format("15:04:05"), hex.EncodeToString(frame))
		}

		mh := reHeader.FindStringSubmatch(raw)
		if len(mh) >= 5 {
			seq := mh[1]
			rcvrOpt := mh[2]
			lpref := mh[3]
			acct := mh[4]

			// VERIFY ACCOUNT ID
			if cfg.SIAAccountID != "" && acct != cfg.SIAAccountID {
				atomic.AddUint64(&stats.RejectedAccountMismatch, 1)
				log.Printf("[%s] SECURITY: Rejected frame from account '%s' (expected '%s') from %s\n",
					time.Now().Format("15:04:05"), acct, cfg.SIAAccountID, ra)
				// Don't send ACK for invalid accounts
				continue
			}

			if cfg.Verbose && cfg.SIAAccountID != "" {
				log.Printf("[%s] VERIFIED: account=%s\n", time.Now().Format("15:04:05"), acct)
			}

			ack := buildAck(seq, rcvrOpt, lpref, acct)
			_, _ = conn.Write(ack)
			atomic.AddUint64(&stats.AcksTx, 1)

			if cfg.Verbose {
				log.Printf("[%s] SENT: %s\n", time.Now().Format("15:04:05"), strings.TrimSpace(string(ack)))
			}
		} else {
			log.Println("DEBUG: frame did not match DC-09 header; skipping ACK + parsing")
			continue
		}

		// Parse event code and user ID
		me := reEventCode.FindStringSubmatch(raw)
		if len(me) >= 2 {
			eventCode := me[1] // "OP", "CL", etc.
			userID := ""
			if len(me) > 2 {
				userID = me[2] // "502", "123", etc.
			}

			if st, ok := codeToState[eventCode]; ok {
				log.Printf("[%s] STATUS: %s (code=%s, user=%s)\n",
					time.Now().Format("15:04:05"), st, eventCode, userID)
				stats.UpdateEvent(st, eventCode, userID, raw)
				if err := persistState(cfg.StateFile, stats); err != nil {
					log.Printf("[%s] WARN: failed to persist state: %v\n", time.Now().Format("15:04:05"), err)
				}
				pub.PublishState(st, eventCode)
				if userID != "" {
					pub.PublishUser(userID)
				}
			} else if cfg.Verbose {
				log.Printf("[%s] INFO: unhandled event code: %s (user=%s)\n",
					time.Now().Format("15:04:05"), eventCode, userID)
			}
		}
	}
}

func main() {
	cfgPath := DefaultConfigPath
	if len(os.Args) >= 2 && strings.TrimSpace(os.Args[1]) != "" {
		cfgPath = os.Args[1]
	}

	cfg, err := loadConfig(cfgPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "config error: %v\n", err)
		os.Exit(2)
	}

	// Setup logging - if log_file is configured, use it; otherwise disable logging
	if cfg.LogFile != "" {
		logDir := filepath.Dir(cfg.LogFile)
		if err := os.MkdirAll(logDir, 0o755); err != nil {
			fmt.Fprintf(os.Stderr, "failed to create log directory: %v\n", err)
			os.Exit(2)
		}

		logger := &lumberjack.Logger{
			Filename:   cfg.LogFile,
			MaxSize:    cfg.LogMaxSizeMB,
			MaxBackups: cfg.LogMaxBackups,
			MaxAge:     cfg.LogMaxAgeDays,
			Compress:   true,
		}

		log.SetOutput(logger)
		log.SetFlags(0) // We add our own timestamps

		log.Printf("[%s] sia2mqtt starting - logging to %s (max_size=%dMB, max_backups=%d, max_age=%dd)\n",
			time.Now().Format("15:04:05"), cfg.LogFile, cfg.LogMaxSizeMB, cfg.LogMaxBackups, cfg.LogMaxAgeDays)
	} else {
		// No log file configured - disable logging by sending to /dev/null
		log.SetOutput(io.Discard)
	}

	stats := &RuntimeStats{
		StartTime: time.Now(),
		LastState: StateUnknown,
	}

	if err := loadPersistedState(cfg.StateFile, stats); err != nil {
		log.Printf("[%s] WARN: failed to load persisted state: %v\n", time.Now().Format("15:04:05"), err)
	}

	log.Printf("[%s] Config: listen=%s account=%s http=%s mqtt_broker=%s mqtt_topic=%s qos=%d retain=%v discovery=%v\n",
		time.Now().Format("15:04:05"), cfg.SIAListenAddr, cfg.SIAAccountID, cfg.HTTPListenAddr,
		cfg.MQTTBroker, cfg.MQTTTopic, cfg.MQTTQOS, cfg.MQTTRetain, cfg.MQTTDiscoveryEnable)

	pub := &Publisher{cfg: cfg, stats: stats}
	if err := pub.Connect(); err != nil {
		log.Fatalf("[%s] mqtt connect error: %v\n", time.Now().Format("15:04:05"), err)
	}

	startHTTP(cfg, pub, stats)

	l, err := net.Listen("tcp", cfg.SIAListenAddr)
	if err != nil {
		log.Fatalf("[%s] listen error: %v\n", time.Now().Format("15:04:05"), err)
	}
	defer l.Close()

	log.Printf("[%s] SIA server listening on %s\n", time.Now().Format("15:04:05"), cfg.SIAListenAddr)

	for {
		c, err := l.Accept()
		if err != nil {
			log.Printf("[%s] accept error: %v\n", time.Now().Format("15:04:05"), err)
			continue
		}
		go handleConnection(c, cfg, pub, stats)
	}
}
