func loadConfig(path string) (Config, error) {
	cfg := defaultConfig()

	f, err := os.Open(path)
	if err != nil {
		return cfg, err
	}
	defer f.Close()

	sc := bufio.NewScanner(f)
	lineno := 0
	for sc.Scan() {
		lineno++
		line := strings.TrimSpace(sc.Text())
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		k, v, ok := strings.Cut(line, "=")
		if !ok {
			return cfg, fmt.Errorf("%s:%d invalid line (expected key=value): %q", path, lineno, line)
		}
		key := strings.ToLower(strings.TrimSpace(k))
		val := strings.TrimSpace(v)

		switch key {
		case "sia_listen":
			cfg.SIAListenAddr = val
		case "sia_account_id":
			cfg.SIAAccountID = val
		case "http_listen":
			cfg.HTTPListenAddr = val
		case "state_file":
			cfg.StateFile = val

		case "mqtt_broker":
			cfg.MQTTBroker = val
		case "mqtt_user":
			cfg.MQTTUser = val
		case "mqtt_password":
			cfg.MQTTPass = val
		case "mqtt_clientid":
			cfg.MQTTClientID = val
		case "mqtt_topic":
			cfg.MQTTTopic = val
		case "mqtt_qos":
			n, e := strconv.Atoi(val)
			if e != nil || n < 0 || n > 2 {
				return cfg, fmt.Errorf("%s:%d mqtt_qos must be 0..2: %q", path, lineno, val)
			}
			cfg.MQTTQOS = byte(n)
		case "mqtt_retain":
			b, e := parseBool(val)
			if e != nil {
				return cfg, fmt.Errorf("%s:%d mqtt_retain invalid: %q", path, lineno, val)
			}
			cfg.MQTTRetain = b

		case "mqtt_availability_topic":
			cfg.MQTTAvailabilityTopic = val
		case "mqtt_availability_online":
			cfg.MQTTAvailabilityOn = val
		case "mqtt_availability_offline":
			cfg.MQTTAvailabilityOff = val

		case "mqtt_discovery_enable":
			b, e := parseBool(val)
			if e != nil {
				return cfg, fmt.Errorf("%s:%d mqtt_discovery_enable invalid: %q", path, lineno, val)
			}
			cfg.MQTTDiscoveryEnable = b
		case "mqtt_discovery_prefix":
			cfg.MQTTDiscoveryPrefix = val
		case "mqtt_discovery_node_id":
			cfg.MQTTDiscoveryNodeID = val
		case "mqtt_discovery_object_id":
			cfg.MQTTDiscoveryObject = val
		case "mqtt_discovery_name":
			cfg.MQTTDiscoveryName = val

		case "mqtt_tls_insecure_skip_verify":
			b, e := parseBool(val)
			if e != nil {
				return cfg, fmt.Errorf("%s:%d mqtt_tls_insecure_skip_verify invalid: %q", path, lineno, val)
			}
			cfg.MQTTInsecureSkipVerify = b

		case "log_file":
			cfg.LogFile = val
		case "log_max_size_mb":
			n, e := strconv.Atoi(val)
			if e != nil || n <= 0 {
				return cfg, fmt.Errorf("%s:%d log_max_size_mb must be positive: %q", path, lineno, val)
			}
			cfg.LogMaxSizeMB = n
		case "log_max_backups":
			n, e := strconv.Atoi(val)
			if e != nil || n < 0 {
				return cfg, fmt.Errorf("%s:%d log_max_backups must be non-negative: %q", path, lineno, val)
			}
			cfg.LogMaxBackups = n
		case "log_max_age_days":
			n, e := strconv.Atoi(val)
			if e != nil || n < 0 {
				return cfg, fmt.Errorf("%s:%d log_max_age_days must be non-negative: %q", path, lineno, val)
			}
			cfg.LogMaxAgeDays = n

		case "verbose":
			b, e := parseBool(val)
			if e != nil {
				return cfg, fmt.Errorf("%s:%d verbose invalid: %q", path, lineno, val)
			}
			cfg.Verbose = b

		default:
			return cfg, fmt.Errorf("%s:%d unknown key: %q", path, lineno, key)
		}
	}
	if err := sc.Err(); err != nil {
		return cfg, err
	}

	if cfg.MQTTBroker == "" {
		return cfg, errors.New("mqtt_broker is required")
	}
	if cfg.MQTTTopic == "" {
		return cfg, errors.New("mqtt_topic is required")
	}
	if cfg.SIAListenAddr == "" {
		return cfg, errors.New("sia_listen is required")
	}
	if cfg.HTTPListenAddr == "" {
		return cfg, errors.New("http_listen is required")
	}
	if cfg.MQTTDiscoveryPrefix == "" {
		cfg.MQTTDiscoveryPrefix = "homeassistant"
	}
	if cfg.MQTTDiscoveryNodeID == "" {
		cfg.MQTTDiscoveryNodeID = "ajax"
	}
	if cfg.MQTTDiscoveryObject == "" {
		cfg.MQTTDiscoveryObject = "alarm_state"
	}
	if cfg.MQTTDiscoveryName == "" {
		cfg.MQTTDiscoveryName = "Ajax Alarm State"
	}
	return cfg, nil
}

func parseBool(s string) (bool, error) {
	switch strings.ToLower(strings.TrimSpace(s)) {
	case "1", "true", "yes", "on":
		return true, nil
	case "0", "false", "no", "off":
		return false, nil
	default:
		return false, fmt.Errorf("invalid bool: %q", s)
	}
}

