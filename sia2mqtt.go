// Ajax hubs use CRC-16/IBM (aka ARC): poly 0xA001, init 0x0000.
func calcCRC(data []byte) string {
	var crc uint16 = 0x0000
	for _, b := range data {
		crc ^= uint16(b)
		for i := 0; i < 8; i++ {
			if (crc & 0x0001) != 0 {
				crc = (crc >> 1) ^ 0xA001
			} else {
				crc >>= 1
			}
		}
	}
	return fmt.Sprintf("%04X", crc)
}

func fmtLEN(n int) string { return fmt.Sprintf("%04X", n) }

const maxFrameSize = 16 * 1024

func readFrame(r *bufio.Reader) ([]byte, error) {
	for {
		b, err := r.ReadByte()
		if err != nil {
			return nil, err
		}
		if b != '\r' && b != '\n' {
			_ = r.UnreadByte()
			break
		}
	}
	var buf []byte
	for {
		b, err := r.ReadByte()
		if err != nil {
			if err == io.EOF && len(buf) > 0 {
				return buf, nil
			}
			return nil, err
		}
		if b == '\r' || b == '\n' {
			return buf, nil
		}
		buf = append(buf, b)
		if len(buf) > maxFrameSize {
			return nil, fmt.Errorf("frame exceeds maximum size (%d bytes)", maxFrameSize)
		}
	}
}

func buildAck(seq, rcvrOpt, lpref, acct string) []byte {
	payload := fmt.Sprintf("\"ACK\"%s%s%s#%s[]", seq, rcvrOpt, lpref, acct)
	l := fmtLEN(len(payload))
	crc := calcCRC([]byte(payload))
	return []byte("\n" + crc + l + payload + "\r")
}

