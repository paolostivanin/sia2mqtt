type Publisher struct {
	cfg   Config
	stats *RuntimeStats

	client mqtt.Client

	mu        sync.Mutex
	lastState AlarmState
	lastCode  string
	lastUser  string

	// ensure we only publish discovery config once per process start
	discoverySent atomic.Bool
}

func (p *Publisher) discoveryConfigTopic() string {
	// homeassistant/sensor/<node_id>/<object_id>/config
	return fmt.Sprintf("%s/sensor/%s/%s/config",
		strings.TrimSuffix(p.cfg.MQTTDiscoveryPrefix, "/"),
		p.cfg.MQTTDiscoveryNodeID,
		p.cfg.MQTTDiscoveryObject,
	)
}

func (p *Publisher) userDiscoveryConfigTopic() string {
	// homeassistant/sensor/<node_id>/user/config
	return fmt.Sprintf("%s/sensor/%s/user/config",
		strings.TrimSuffix(p.cfg.MQTTDiscoveryPrefix, "/"),
		p.cfg.MQTTDiscoveryNodeID,
	)
}

func (p *Publisher) uniqueID() string {
	return fmt.Sprintf("%s_%s", p.cfg.MQTTDiscoveryNodeID, p.cfg.MQTTDiscoveryObject)
}

func (p *Publisher) userTopic() string {
	// home/alarm/ajax/user
	return strings.TrimSuffix(p.cfg.MQTTTopic, "/state") + "/user"
}

func (p *Publisher) publishDiscoveryConfig() {
	if !p.cfg.MQTTDiscoveryEnable {
		return
	}
	if p.discoverySent.Load() {
		return
	}
	if p.client == nil || !p.client.IsConnected() {
		return
	}

	// 1. Publish STATE sensor discovery
	statePayload := map[string]any{
		"name":                  p.cfg.MQTTDiscoveryName,
		"unique_id":             p.uniqueID(),
		"state_topic":           p.cfg.MQTTTopic,
		"availability_topic":    p.cfg.MQTTAvailabilityTopic,
		"payload_available":     p.cfg.MQTTAvailabilityOn,
		"payload_not_available": p.cfg.MQTTAvailabilityOff,
		"value_template":        `{{ value_json.state }}`,
		"device_class":          "enum",
		"options":               []string{string(StateUnknown), string(StateDisarmed), string(StateArmed), string(StateNight), string(StateAlarm)},
		"icon":                  "mdi:shield-home",

		"device": map[string]any{
			"identifiers":  []string{"sia2mqtt_ajax"},
			"name":         "sia2mqtt Ajax",
			"manufacturer": "Custom",
			"model":        "SIA DC-09 Receiver",
		},
	}

	stateBody, _ := json.Marshal(statePayload)
	stateTopic := p.discoveryConfigTopic()
	t1 := p.client.Publish(stateTopic, p.cfg.MQTTQOS, true, stateBody)
	t1.Wait()
	if t1.Error() == nil {
		log.Printf("[%s] MQTT discovery (state) published: %s\n", time.Now().Format("15:04:05"), stateTopic)
	} else {
		log.Printf("[%s] MQTT discovery (state) publish error: %v\n", time.Now().Format("15:04:05"), t1.Error())
		return
	}

	// 2. Publish USER sensor discovery
	userPayload := map[string]any{
		"name":                  p.cfg.MQTTDiscoveryName + " Last User",
		"unique_id":             p.uniqueID() + "_user",
		"state_topic":           p.userTopic(),
		"availability_topic":    p.cfg.MQTTAvailabilityTopic,
		"payload_available":     p.cfg.MQTTAvailabilityOn,
		"payload_not_available": p.cfg.MQTTAvailabilityOff,
		"value_template":        `{{ value_json.user }}`,
		"icon":                  "mdi:account",

		"device": map[string]any{
			"identifiers":  []string{"sia2mqtt_ajax"},
			"name":         "sia2mqtt Ajax",
			"manufacturer": "Custom",
			"model":        "SIA DC-09 Receiver",
		},
	}

	userBody, _ := json.Marshal(userPayload)
	userTopic := p.userDiscoveryConfigTopic()
	t2 := p.client.Publish(userTopic, p.cfg.MQTTQOS, true, userBody)
	t2.Wait()
	if t2.Error() == nil {
		log.Printf("[%s] MQTT discovery (user) published: %s\n", time.Now().Format("15:04:05"), userTopic)
		p.discoverySent.Store(true)
	} else {
		log.Printf("[%s] MQTT discovery (user) publish error: %v\n", time.Now().Format("15:04:05"), t2.Error())
	}
}

func (p *Publisher) Connect() error {
	opts := mqtt.NewClientOptions()
	opts.AddBroker(p.cfg.MQTTBroker)

	clientID := p.cfg.MQTTClientID
	if clientID == "" {
		clientID = fmt.Sprintf("sia2mqtt-%d", time.Now().UnixNano())
	}
	opts.SetClientID(clientID)

	if p.cfg.MQTTUser != "" {
		opts.SetUsername(p.cfg.MQTTUser)
		opts.SetPassword(p.cfg.MQTTPass)
	}

	opts.SetAutoReconnect(true)
	opts.SetConnectRetry(true)
	opts.SetConnectRetryInterval(3 * time.Second)
	opts.SetKeepAlive(30 * time.Second)
	opts.SetPingTimeout(10 * time.Second)

	if strings.HasPrefix(strings.ToLower(p.cfg.MQTTBroker), "tls://") || strings.HasPrefix(strings.ToLower(p.cfg.MQTTBroker), "ssl://") {
		tlsCfg := &tls.Config{InsecureSkipVerify: p.cfg.MQTTInsecureSkipVerify}
		opts.SetTLSConfig(tlsCfg)
	}

	if p.cfg.MQTTAvailabilityTopic != "" {
		opts.SetWill(p.cfg.MQTTAvailabilityTopic, p.cfg.MQTTAvailabilityOff, p.cfg.MQTTQOS, true)
	}

	opts.OnConnect = func(c mqtt.Client) {
		if p.cfg.MQTTAvailabilityTopic != "" {
			t := c.Publish(p.cfg.MQTTAvailabilityTopic, p.cfg.MQTTQOS, true, p.cfg.MQTTAvailabilityOn)
			t.Wait()
		}
		// Publish discovery config on (re)connect
		p.publishDiscoveryConfig()
	}

	opts.OnConnectionLost = func(_ mqtt.Client, err error) {
		log.Printf("[%s] MQTT connection lost: %v\n", time.Now().Format("15:04:05"), err)
		p.discoverySent.Store(false)
	}

	p.client = mqtt.NewClient(opts)
	tok := p.client.Connect()
	tok.Wait()
	if err := tok.Error(); err != nil {
		return err
	}

	log.Printf("[%s] MQTT connected as %s to %s\n", time.Now().Format("15:04:05"), clientID, p.cfg.MQTTBroker)
	// Ensure discovery is published even if OnConnect already fired
	p.publishDiscoveryConfig()
	return nil
}

func (p *Publisher) IsConnected() bool {
	return p.client != nil && p.client.IsConnected()
}

// Publishes JSON to mqtt_topic:
// {"state":"armed","code":"CL","ts":"2026-01-30T22:22:14+01:00"}
func (p *Publisher) PublishState(state AlarmState, code string) {
	p.mu.Lock()
	defer p.mu.Unlock()

	if state == p.lastState && code == p.lastCode {
		return
	}
	p.lastState = state
	p.lastCode = code

	if p.client == nil || !p.client.IsConnected() {
		log.Printf("[%s] MQTT not connected; dropping publish state=%s code=%s\n", time.Now().Format("15:04:05"), state, code)
		p.stats.UpdateMQTTPublish(false)
		return
	}

	msg := map[string]any{
		"state": string(state),
		"code":  code,
		"ts":    time.Now().Format(time.RFC3339),
	}
	body, _ := json.Marshal(msg)

	t := p.client.Publish(p.cfg.MQTTTopic, p.cfg.MQTTQOS, p.cfg.MQTTRetain, body)
	t.Wait()
	if err := t.Error(); err != nil {
		log.Printf("[%s] MQTT publish error: %v\n", time.Now().Format("15:04:05"), err)
		p.stats.UpdateMQTTPublish(false)
		return
	}

	log.Printf("[%s] MQTT publish: topic=%s payload=%s\n", time.Now().Format("15:04:05"), p.cfg.MQTTTopic, string(body))
	p.stats.UpdateMQTTPublish(true)
}

// Publishes user ID to separate topic:
// {"user":"502","ts":"2026-01-30T22:22:14+01:00"}
func (p *Publisher) PublishUser(userID string) {
	p.mu.Lock()
	defer p.mu.Unlock()

	if userID == p.lastUser {
		return
	}
	p.lastUser = userID

	if p.client == nil || !p.client.IsConnected() {
		log.Printf("[%s] MQTT not connected; dropping publish user=%s\n", time.Now().Format("15:04:05"), userID)
		return
	}

	msg := map[string]any{
		"user": userID,
		"ts":   time.Now().Format(time.RFC3339),
	}
	body, _ := json.Marshal(msg)

	userTopic := p.userTopic()
	t := p.client.Publish(userTopic, p.cfg.MQTTQOS, p.cfg.MQTTRetain, body)
	t.Wait()
	if err := t.Error(); err != nil {
		log.Printf("[%s] MQTT user publish error: %v\n", time.Now().Format("15:04:05"), err)
		return
	}

	if p.cfg.Verbose {
		log.Printf("[%s] MQTT user publish: topic=%s payload=%s\n", time.Now().Format("15:04:05"), userTopic, string(body))
	}
}

